package com.example.demo.service.code.vo;

public class CodeVO {

	private String code_cd;
	private String p_code_cd;
	private String code_name;
	private String code_value;
	private String description;
	private String is_active;
	private String order_num;
	
	public String getCode_cd() {
		return code_cd;
	}
	public void setCode_cd(String code_cd) {
		this.code_cd = code_cd;
	}
	public String getP_code_cd() {
		return p_code_cd;
	}
	public void setP_code_cd(String p_code_cd) {
		this.p_code_cd = p_code_cd;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getCode_value() {
		return code_value;
	}
	public void setCode_value(String code_value) {
		this.code_value = code_value;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getIs_active() {
		return is_active;
	}
	public void setIs_active(String is_active) {
		this.is_active = is_active;
	}
	public String getOrder_num() {
		return order_num;
	}
	public void setOrder_num(String order_num) {
		this.order_num = order_num;
	}

}
